function generate(){
	for(x=0; x<11; x++){
		for(y=0; y<11; y++){
			$(".body_game").append('<div class="cell" id="x'+x+'y'+y+'"></div>');
		}
	}
	
	for(k=0; k<40; k++){
		$('#x' + j[1].path[k][0] + 'y' + j[1].path[k][1]).addClass('path-cell');
	}

	for(x=1; x<5; x++){
		for(y=0; y<4; y++){
			$('#x' + j[x].B[y] + 'y' + j[x].B[y+4]).addClass('B').addClass('B'+x+'-cell').append('<div class="player B'+x+'" id="'+ x + y +'"></div>');
		}

		$('#x' + j[x].start[0] + 'y' + j[x].start[1]).addClass('B'+x+'-cell');

		for(k=0; k<4; k++){
			$('#x' + j[x].finish[k] + 'y' + j[x].finish[k+4]).addClass('B'+x+'-cell');
		}
	}


	$('#x3y0').addClass('arrow').addClass('right').removeClass('cell');
	$('#x0y7').addClass('arrow').addClass('down').removeClass('cell');
	$('#x7y10').addClass('arrow').addClass('left').removeClass('cell');
	$('#x10y3').addClass('arrow').addClass('up').removeClass('cell');
}